#include <stdio.h>
#include <stdbool.h>

void printingMaze(bool walls[16][16])
{
	int i, j, count = 12;
	for(i = 0; i < 4; i++)
	{
		printf("  _ ");
	}
	printf("\n");
	for(i = 0; i < 4; i++)
	{
		printf("| ");
		for(j = 0; j < 4; j++)
		{
			if(!walls[count][count - 4] || count == 0 || count == 1 || count == 2 || count == 3)
			{
				printf("_ ");
			}
			else
			{
				printf("  ");
			}
			if(!walls[count][count + 1] || count == 15 || count == 11 || count == 7 || count == 3)
			{
				printf("| ");
			}
			else
			{
				printf("  ");
			}
			//printf("count = %i\n", count);
			count++;
		}
		printf("\n");
		count -= 8;
	}
}

void printWalls(bool walls[16][16])
{
	int i, j;
	for(i = 0; i < 16; i++)
		printf("%i\t", i);
	printf("\n");
	for(i = 0; i < 16; i++)
	{
		printf("%i: ", i);
		for(j = 0; j < 16; j++)
			printf("%d\t", walls[i][j]);
		printf("\n");
	}
	printf("\n");
}


